package pojo;

public class UserNumber
{
    private int favoriteNumber;

    private int preferredNumberOfChildren;

    public int getFavoriteNumber() {
        return favoriteNumber;
    }

    public void setFavoriteNumber(int favoriteNumber) {
        this.favoriteNumber = favoriteNumber;
    }

    public int getPreferredNumberOfChildren() {
        return preferredNumberOfChildren;
    }

    public void setPreferredNumberOfChildren(int preferredNumberOfChildren) {
        this.preferredNumberOfChildren = preferredNumberOfChildren;
    }
}
